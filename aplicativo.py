import tkinter as tk # Importa a biblioteca Tkinter para criar a interface gráfica

# Cria a janela principal
root = tk.Tk() # 'Tk()' cria a janela principal do nosso programa
root.title("Meu checklist") # Define o título que aparece na barra da janela
root.geometry("400x300") # Define o tamanho da janela (largura x altura em pixels)

# Loop principal da interface
root.mainloop() # Mantém a janela aberta até o usuário fechá-la